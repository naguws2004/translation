// Using single quotes
const greeting = '@@tls_key_hello_world';

// Using double quotes
const name = "@@tls_key_alice";

// Using template literals for string interpolation
const message = `Hello, ${name}!`;

// Multi-line strings using template literals
const poem = `@@tls_key_roses_are_red_violets_ar`;

// Escaping characters within strings
const quote = "She said, \"JavaScript is fun!\"";

console.log(greeting); // Outputs: @@tls_key_hello_world
console.log(message); // Outputs: Hello, @@tls_key_alice!
console.log(poem);
console.log(quote); // Outputs: @@tls_key_she_said_javascript_is_f